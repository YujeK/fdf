#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long int n;

	n = nb;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * -1;
	}
	if (n < 10)
	{
		ft_putchar_fd(n + '0', fd);
		return ;
	}
	ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(48 + n % 10, fd);
}
