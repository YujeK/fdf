#include "libft.h"

long int	ft_lenint(long int n)
{
	long int i;

	i = 0;
	if (!n)
		return (1);
	if (n < 0)
	{
		i++;
		n = -n;
	}
	while (n > 0)
	{
		i++;
		n = (n / 10);
	}
	return (i);
}
